            if(!empty($request->from_date)){
                $data = DB::table('sales')
                ->whereBetween('created_at', array($request->from_date, $request->to_date))
                ->get();
            }else{
                $data = DB::table('sales')
                ->get();
            }
            return datatables()->of($data)->make(true);
        }
        return view('admin.products.saleshistory');        
    }

